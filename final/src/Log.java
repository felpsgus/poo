public class Log {
	private int idLog;
	private int idPessoa;
	private String dtaLog;

	public int getIdLog() {
		return idLog;
	}

	public void setIdLog(int idLog) {
		this.idLog = idLog;
	}

	public int getIdPessoa() {
		return idPessoa;
	}

	public void setIdPessoa(int idPessoa) {
		this.idPessoa = idPessoa;
	}

	public String getDtaLog() {
		return dtaLog;
	}

	public void setDtaLog(String dtaLog) {
		this.dtaLog = dtaLog;
	}
}
